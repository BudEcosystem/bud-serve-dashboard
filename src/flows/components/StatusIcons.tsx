export function StatusEvents({
    events =[],
}: {
    events: any[]
}) {
    const failedEvents = events.filter((event) => event?.payload?.content?.status === 'FAILED');
    return <div className="px-[1.35rem] w-full pt-[1.3rem] pb-[1rem]">
        {events?.map((event, index) => {
            if (event?.payload?.content?.status === 'COMPLETED') {
                return <Completed text={event.title} key={index} />
            }
            if (event?.payload?.content?.status === 'STARTED') {
                return <InProgress text={event.title} key={index} />
            }
            if (!event?.payload?.content?.status) {
                if (failedEvents.length > 0) {
                    return <Skipped text={event.title} key={index} />
                }
                return <Pending text={event.title} key={index} />
            }
            if (event?.payload?.content?.status === 'FAILED') {
                return <div key={index}>
                    <Failed text={event.title} payload={event.payload} />
                    <FailedEvent payload={event?.payload} />
                </div>
            }
        })}
    </div>
}